<p>Пошкодження автомобіля може статися в різний спосіб. Це може бути зіткнення транспортних
засобів, погане дорожнє покриття, падіння на автомобіль будь-яких предметів (дерев, опор). Все
це призводить до заподіяння матеріальної шкоди. Чинним законодавством України обов &#39;язок
щодо відшкодування матеріальної шкоди покладається на особу, з чиєї вини її було заподіяно. У
більшості випадків відповідальність таких осіб застрахована, але страхові компанії не завжди
виконують свої обов &#39;язки сумлінно, а іноді страхової суми не достатньо для покриття вартості
ремонту та різницю треба стягувати з винуватця.</p>
